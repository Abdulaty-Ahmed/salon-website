// Language management functionality
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('selectedLanguage') || 'tr';
        this.init();
    }

    init() {
        this.bindEvents();
        this.setLanguage(this.currentLanguage);
        this.updateLanguageDisplay();
    }

    bindEvents() {
        // Language dropdown toggle
        const languageBtn = document.querySelector('.language-btn');
        const languageOptions = document.querySelector('.language-options');

        if (languageBtn && languageOptions) {
            languageBtn.addEventListener('click', (e) => {
                e.stopPropagation();
                languageOptions.classList.toggle('show');
            });

            // Close dropdown when clicking outside
            document.addEventListener('click', () => {
                languageOptions.classList.remove('show');
            });

            // Language option clicks
            const langOptions = document.querySelectorAll('.lang-option');
            langOptions.forEach(option => {
                option.addEventListener('click', (e) => {
                    e.preventDefault();
                    const selectedLang = option.getAttribute('data-lang');
                    this.setLanguage(selectedLang);
                    languageOptions.classList.remove('show');
                });
            });
        }
    }

    setLanguage(lang) {
        if (!translations[lang]) {
            console.warn(`Language ${lang} not found, falling back to Turkish`);
            lang = 'tr';
        }

        this.currentLanguage = lang;
        localStorage.setItem('selectedLanguage', lang);
        
        // Update HTML attributes
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
        document.body.className = lang === 'ar' ? 'rtl' : 'ltr';
        
        // Apply translations
        this.applyTranslations();
        this.updateLanguageDisplay();
        
        // Update font family for Arabic
        if (lang === 'ar') {
            document.body.style.fontFamily = "'Noto Sans Arabic', 'Poppins', sans-serif";
        } else {
            document.body.style.fontFamily = "'Poppins', sans-serif";
        }
    }

    applyTranslations() {
        const langData = translations[this.currentLanguage];
        
        // Translate elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (langData[key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.value = langData[key];
                } else {
                    element.innerHTML = langData[key];
                }
            }
        });

        // Translate placeholder attributes
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            if (langData[key]) {
                element.placeholder = langData[key];
            }
        });

        // Update meta tags
        const titleElement = document.querySelector('title');
        const descriptionMeta = document.querySelector('meta[name="description"]');
        const keywordsMeta = document.querySelector('meta[name="keywords"]');

        if (titleElement && langData.page_title) {
            titleElement.textContent = langData.page_title;
        }
        if (descriptionMeta && langData.page_description) {
            descriptionMeta.setAttribute('content', langData.page_description);
        }
        if (keywordsMeta && langData.page_keywords) {
            keywordsMeta.setAttribute('content', langData.page_keywords);
        }

        // Update select options
        this.updateSelectOptions();
    }

    updateSelectOptions() {
        const langData = translations[this.currentLanguage];
        
        // Update service select options
        const serviceSelect = document.getElementById('service');
        if (serviceSelect) {
            const options = serviceSelect.querySelectorAll('option[data-translate]');
            options.forEach(option => {
                const key = option.getAttribute('data-translate');
                if (langData[key]) {
                    option.textContent = langData[key];
                }
            });
        }

        // Update location select options
        const locationSelect = document.getElementById('location');
        if (locationSelect) {
            const options = locationSelect.querySelectorAll('option[data-translate]');
            options.forEach(option => {
                const key = option.getAttribute('data-translate');
                if (langData[key]) {
                    option.textContent = langData[key];
                }
            });
        }

        // Update time select options
        const timeSelect = document.getElementById('time');
        if (timeSelect) {
            const options = timeSelect.querySelectorAll('option[data-translate]');
            options.forEach(option => {
                const key = option.getAttribute('data-translate');
                if (langData[key]) {
                    option.textContent = langData[key];
                }
            });
        }
    }

    updateLanguageDisplay() {
        const currentLangSpan = document.querySelector('.current-lang');
        const langMap = {
            'tr': 'TR',
            'en': 'EN',
            'ar': 'ع'
        };
        
        if (currentLangSpan) {
            currentLangSpan.textContent = langMap[this.currentLanguage] || 'TR';
        }

        // Update active state in dropdown
        document.querySelectorAll('.lang-option').forEach(option => {
            option.classList.remove('active');
            if (option.getAttribute('data-lang') === this.currentLanguage) {
                option.classList.add('active');
            }
        });
    }

    // Method to get translated text (for use in JavaScript)
    getText(key) {
        return translations[this.currentLanguage][key] || translations['tr'][key] || key;
    }
}

